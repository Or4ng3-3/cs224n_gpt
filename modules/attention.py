import torch

from einops import rearrange
from torch import nn


class CausalSelfAttention(nn.Module):
  def __init__(self, config):
    super().__init__()

    self.num_attention_heads = config.num_attention_heads
    self.attention_head_size = int(config.hidden_size / config.num_attention_heads)
    self.all_head_size = self.num_attention_heads * self.attention_head_size

    # Initialize the linear transformation layers for key, value, query.
    self.query = nn.Linear(config.hidden_size, self.all_head_size)
    self.key = nn.Linear(config.hidden_size, self.all_head_size)
    self.value = nn.Linear(config.hidden_size, self.all_head_size)
    # This dropout is applied to normalized attention scores following the original
    # implementation of transformer. Although it is a bit unusual, we empirically
    # observe that it yields better performance.
    self.dropout = nn.Dropout(config.attention_probs_dropout_prob)

  def transform(self, x, linear_layer):
    # The corresponding linear_layer of k, v, q are used to project the hidden_state (x).
    proj = linear_layer(x) # proj = x @ W + b
    # Next, we need to produce multiple heads for the proj. This is done by spliting the
    # hidden state to self.num_attention_heads, each of size self.attention_head_size.
    proj = rearrange(proj, 'b t (h d) -> b t h d', h=self.num_attention_heads)
    # By proper transpose, we have proj of size [bs, num_attention_heads, seq_len, attention_head_size].
    proj = rearrange(proj, 'b t h d -> b h t d')
    return proj

  def attention(self, key, query, value, attention_mask):
    """
    input: 
    output: batch_size seq_len hidden_dim
    """

    ### YOUR CODE HERE
    dot_prod = query @ key.transpose(-1, -2) / self.attention_head_size ** 0.5    # b h t d @ b h d t = b h t t

    # attention_mask: [bs, 1, 1, seq_len]
    masked_dot_prod = attention_mask + dot_prod 
    
    # causual mask:
    t = self.key(-2)
    causual_mask = torch.triu(torch.ones(t, t), diagonal=1).bool
    
    dot_prod = dot_prod.masked_fill(causual_mask, float('-inf'))

    soft_dot_prod = torch.softmax(masked_dot_prod, dim = -1)

    # dropout
    soft_dot_prod = self.dropout(soft_dot_prod)
    
    attention = soft_dot_prod @ value   # b h t t @ b h t d = b h t d
    attention = rearrange(attention, 'b h t d -> b t (h d)')

    return attention
